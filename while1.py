
# Пройдите в цикле по списку ["Вася", "Маша", "Петя", "Валера", "Саша", "Даша"],
# пока не встретите имя "Валера". 
# Когда найдете напишите "Валера нашелся". 
# Подсказка: используйте метод list.pop()

my_list = ["Вася", "Маша", "Петя", "Валера", "Саша", "Даша"]
while len(my_list) > 0:
    who_are_you = my_list.pop()
    if who_are_you == 'Валера':
        print('Валера нашелся')
        break